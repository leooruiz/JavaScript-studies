let verdadeiro = true
let falso = false
console.log()

console.log(verdadeiro === falso) //output: false
console.log()

console.log(verdadeiro !== falso)// output: true, pois true e false são do mesmo tipo e são diferentes, afirmação correta
console.log()

console.log(verdadeiro == 1)// output: true, pois true == 1
console.log()

console.log(verdadeiro === 1)// output: false, pois true == 1, mas não possui o mesmo tipo, então é falsa a afirmação
console.log()

console.log('0' == 0)// output: true, pois '0' == 0, mesmo sendo de tipos diferentes
console.log()

console.log(falso === 0)// output: false, pois false == 0, mas não possui o mesmo tipo, então é falsa a afirmação
console.log()

console.log(falso !== 24)// output: true, pois false tem tipo e valor diferente de 24
console.log()
